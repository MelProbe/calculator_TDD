package com.melprobe.tdd.calculator;

public class Calculator {

    public Object add(int i, int j) {
        return i + j;
    }

    public Object subtract(int i, int j) {
        return i - j;
    }

    public Object multiply(int i, int j) {
        return i * j;
    }

    public Object divide(int i, int j) {
        return i / j;
    }

}
